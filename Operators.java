public class Operators {
    public static void main(String[] args) {
        int a = 1, b = 2;
        int sum = a + b;
        int diff = a - b;
        int mul = a * b;
        int div = a / b;
        int modulo = a % b;
        
        // unary operators -> ++, --
        int num = 3;
        num++;
        num--;
        System.out.println(num);

        // conditional operators
        // <, >, ==, <=, >=

        // logical operators
        // &&, ||, !
    }
}
